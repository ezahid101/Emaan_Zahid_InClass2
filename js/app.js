function showA() {
  let A = document.querySelector(".imageA");
  A.style.visibility = "visible";
  let hideA = document.querySelector(".hideA");
  hideA.style.visibility = "visible";
}

function hideA() {
  let A = document.querySelector(".imageA");
  A.style.visibility = "hidden";
  let hideA = document.querySelector(".hideA");
  hideA.style.visibility = "hidden";
}

